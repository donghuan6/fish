package com.nine.common.constans;

/**
 * 正则
 */
public interface Regular {

    /**
     * 手机号
     */
    String PHONE = "^1[3-9]\\d{9}$";

    /**
     * 邮箱
     */
    String EMAIL = "^\\w+([-+.]\\w+)*@\\w+([-.]\\w+)*\\.\\w+([-.]\\w+)*$";

    /**
     * 密码
     * 数字 (0-9)
     * 字母 (a-zA-Z)
     * 特殊字符 (!@#$%^&*()-_=+[]{}|;':",.<>/?)
     */
    String PASSWORD = "^(?![0-9]+$)(?![a-zA-Z]+$)(?![^!@#$%^&*()-_=+\\[\\]{}|;':\",.<>/?0-9]+$)(?![^!@#$%^&*()-_=+\\[\\]{}|;':\",.<>/?a-zA-Z]+$)[0-9A-Za-z!@#$%^&*()-_=+\\[\\]{}|;':\",.<>/?]{6,20}$";



}
